# There are many math functions available in python.
addition = 10 + 5
print(f"10 + 5 = {addition}")
subtraction = 10 - 5
print(f"10 - 5 = {subtraction}")
multiply = 10 * 5
print(f"10 * 5 = {multiply}")
division = 10 / 3      # This is equal to 3.333...
print(f"10 / 3 = {division}")
floor_division = 10//3 # This is equal to 3
print(f"10 // 3 = {floor_division}")
module = 10 % 3        # This is equal to 1
print(f"10 % 3 = {module}")
power = pow(2, 10)     # This is equal to 2^10 = 1024
print(f"2^10 = {power}")

absolute = abs(-1)     # This is equal to 1
print(f"|-1| = {absolute}")

# There are more functions available in the math library. Here is the sqrt (square root) function:
from math import sqrt
square_root = sqrt(16)   # This is equal to 4

print(f"sqrt(16) = {square_root}")